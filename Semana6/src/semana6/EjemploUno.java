public class EjemploUno {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int limite = 10;
        int contador = 1;
        
        while (contador <= limite){
            System.out.printf("Contador %d\n", contador);
            contador = contador + 1;
        }
    }
    
}
